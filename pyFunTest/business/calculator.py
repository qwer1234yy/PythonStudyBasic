class Calculator(object):
    def __init__(self, a, b):
        self.a = a
        self.b = b

    def add(self):
        return self.a + self.b

    def subtract(self):
        return self.a - self.b

    def divide(self):
        return self.a * self.b

    def multible(self):
        return self.a / self.b
